    <h2>Papierkorb</h2>
    <hr>
    <p>Dateien Löschen und wiederherstellen</p>
    <img style="width:200px;" class="article_img_right" src="articles/ohne_zugehörigkeit/papierkorb/fulltrash.png" alt="Bild vom Dock">
    <img style="width:200px;" class="article_img_right" src="articles/ohne_zugehörigkeit/papierkorb/trash.png" alt="Bild vom Dock">
    <ul>
        <li><p>Der <strong>Papierkorb</strong> ist der Aufbewahrungsort von allen gelöschen Dateien und Ordnern, du kannst ihn mit einem Klick auf das Symbol im <a href="article.php?path=die_basics/die_macos_oberfläche.php">Dock</a> öffnen.</p></li>
        <li><p>Löschen kannst du Dateien so wie Ordner mit einem <a href="">ctrl-Click</a> auf das Objekt im <a href="article.php?path=die_standart_apps/finder.php">Finder</a>.</p></li>
        <li><p>Die Elemente im Papierkorb kannst du wiederherstellen, dann werden sie an ihren vorherigen Platz verschoben, oder sofort löschen, dann wird die Datei entgültig gelöscht. Auch kannst du mit einem Click auf "Entleren" auch alle Elemente des Papierkorbes auf einmal Löschen.</p></li>
    </ul>
</div>