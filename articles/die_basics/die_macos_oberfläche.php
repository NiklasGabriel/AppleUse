    <h2>1.2 Die macOS Oberfläche</h2>
    <p>Bekomme einen Überblick über die Bedienelemente deines Macs.</p>
    <ol>
        <img style="width:650px;" class="article_img_right" src="articles/die_basics/die_macos_oberfläche/dock.png" alt="Bild vom Dock">
        <li><p><strong>Der Dock</strong> bieted dir einen schnellen Zugriff auf deine Apps.<br>
        Das erste Symbol, das mit dem lächelnden Gesicht, ist der <a href="article.php?path=die_standart_apps/finder.php">Finder</a>.<br>
        Das letzte Symbol, der Mülleimer, ist dein <a href="article.php?path=ohne_zugehörigkeit/papierkorb.php">Papierkorb</a>.</p></li>
        <img style="width:750px;" class="article_img_right" src="articles/die_basics/die_macos_oberfläche/menübar.png" alt="Bild von der Menübar">
        <li><p><strong>Die Menübar</strong> bieted dir eine große Auswahl an Werkzeugen und Funktionen.</p></li>
        <li><p><strong>Der Schribtisch</strong> , der freie Platz in der Mitte, ist dein Arbeitsplatz, wie auf dem physischen Schreibtisch kannst du hier Dokumente und Dateien ablgen. Der Schreibtisch wird dir auch im <a href="article.php?path=die_standart_apps/finder.php">Finder</a> angezeigt.</p></li>
        <img class="article_img_right" src="articles/die_basics/die_macos_oberfläche/anmeldefenster.png" alt="Bild vom Anmeldefenser">
        <li><p><strong>Das Anmeldefenster</strong> erscheint, dann wenn du gerade nicth angemeldet bist z.B. nach dem Anschlten oder wenn du dich absichtlich abgemeldet hast.</p></li>
    </ol>
